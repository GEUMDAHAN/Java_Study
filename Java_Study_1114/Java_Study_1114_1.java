package Java_Study_1114;

public class JavaStudy1114 {

	public static void main(String[] args) {// 메인함수 중괄호 시작
		// 중괄호 안에 코드를 작성해야 합니다.
		System.out.println("Hello");// 항상 코드 마지막엔 세미콜론(;)
		System.out.println("World!");// Ctrl + F11(코드실행)
		
		/*
		 * 주석의 종류
		 * 'Ctrl + / = 한 줄로 작성'
		 * '/ + * + Enter = 문단으로 작성'
		 */
		
		int x = 10;
		
		/*
		 * int : 데이터 타입
		 * x : 식별자(OO변수명)
		 * 식별자(변수명)의 원칙 → PPT 6장 참조
		 * 1. 변수명 앞에 숫자 X
		 * 2. 자바 키워드 사용 X
		 * 예시) int 100x = 20; // 오류 : 변수명(x) 앞에 숫자(100)가 왔습니다.
		 */
		
		String userName = "홍길동";
		
		/*
		 * String : 문자열 타입
		 * userName : 식별자(변수명)
		 * user(명사) + name(명사) → 명사와 명사가 만나면 뒤에 있는 명사는 앞에 대문자로 작성
		 * ㄴ카멜표기법 → 카멜(낙타)의 혹 닮아서 지음
		 */
		
	}// 메인함수 중괄호 종료

}
